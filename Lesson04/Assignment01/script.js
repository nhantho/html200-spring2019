function squareInput() {
  let input = prompt('Enter a number to square');
  let result = 0;

  result = input * input;

  alert(result);
}

function multiplyNumber() {
  let inputOne = prompt('Enter your first number');
  let inputTwo = prompt('Enter your second number');
  let result = 0;

  result = inputOne * inputTwo;

  alert(result);
}
